'''
While: Estructura cíclica condicional, valida la condición definida dentro de los paréntesis y si se cumple,
es decir, si el resultado de la evaluación es true entonces ejecuta el bloque de código, al terminar
repite el proceso hasta que sea false y sigue con el flujo del código

Ej:
    while({condición}):
        {
            Bloque de código
        }
'''

while(True):
    print("Hola Linda")
